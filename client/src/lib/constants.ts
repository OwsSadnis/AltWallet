export type RiskTone = "safe" | "medium" | "high";

export function riskFromScore(score: number): { tone: RiskTone; label: string } {
  if (score >= 70) return { tone: "safe", label: "Safe" };
  if (score >= 40) return { tone: "medium", label: "Medium Risk" };
  return { tone: "high", label: "High Risk" };
}

